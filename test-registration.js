/**
 * Registration Flow Test Script
 * Simulates the complete registration process
 */

// Simulated user input from the registration form
const testUserInput = {
  name: "John Doe",
  email: "john.doe@example.com", 
  phone: "+254757650125",
  birthDate: new Date("1990-05-15"),
  dietaryPreferences: "Vegetarian",
  favoriteTable: "Window"
};

console.log("=== REGISTRATION SIMULATION START ===\n");
console.log("1. USER INPUT (from registration form):");
console.log(JSON.stringify(testUserInput, null, 2));

// Import the mapping function logic
const mapGuestToPatient = (guestData) => {
  return {
    // Required fields
    userId: guestData.userId,
    name: guestData.name,
    email: guestData.email,
    phone: guestData.phone,
    birthDate: guestData.birthDate || new Date("1990-01-01"),
    gender: guestData.gender || "Prefer not to say",
    address: guestData.address || guestData.favoriteTable || "No preference",
    occupation: guestData.occupation || "Restaurant Guest",
    emergencyContactName: guestData.emergencyContactName || guestData.name,
    emergencyContactNumber: guestData.emergencyContactNumber || guestData.phone,
    
    // CRITICAL: These three fields are REQUIRED by Appwrite
    primaryPhysician: guestData.primaryPhysician || "General",
    insuranceProvider: guestData.insuranceProvider || guestData.dietaryPreferences || "None",
    insurancePolicyNumber: guestData.insurancePolicyNumber || `GUEST-${Date.now()}`,
    
    // CRITICAL: This MUST be true
    privacyConsent: true,
    
    // Optional fields
    allergies: guestData.allergies || guestData.dietaryPreferences,
    currentMedication: guestData.currentMedication || "",
    identificationType: guestData.identificationType || "Guest Registration",
    identificationNumber: guestData.identificationNumber || `GUEST-${guestData.userId?.substring(0, 8)}`,
    identificationDocument: undefined,
  };
};

// Step 1: Map guest data to patient schema
console.log("\n2. MAPPED DATA (after mapGuestToPatient):");
const mappedData = mapGuestToPatient({
  ...testUserInput,
  userId: "user_abc123" // Simulated user ID from auth
});
console.log(JSON.stringify(mappedData, null, 2));

// Step 2: Create final RegisterGuestParams object
console.log("\n3. FINAL REGISTER PARAMS (sent to Appwrite):");
const registerParams = {
  // User object fields
  name: mappedData.name,
  email: mappedData.email,
  phone: mappedData.phone,
  
  // All required fields for Appwrite
  userId: mappedData.userId,
  birthDate: mappedData.birthDate,
  gender: mappedData.gender,
  address: mappedData.address,
  occupation: mappedData.occupation,
  emergencyContactName: mappedData.emergencyContactName,
  emergencyContactNumber: mappedData.emergencyContactNumber,
  primaryPhysician: mappedData.primaryPhysician,
  insuranceProvider: mappedData.insuranceProvider,
  insurancePolicyNumber: mappedData.insurancePolicyNumber,
  allergies: mappedData.allergies,
  currentMedication: mappedData.currentMedication,
  identificationType: mappedData.identificationType,
  identificationNumber: mappedData.identificationNumber,
  identificationDocument: undefined,
  privacyConsent: mappedData.privacyConsent
};
console.log(JSON.stringify(registerParams, null, 2));

// Validate all required fields are present
console.log("\n4. VALIDATION CHECK:");
const requiredFields = [
  'userId', 'name', 'email', 'phone', 'birthDate', 'gender',
  'address', 'occupation', 'emergencyContactName', 'emergencyContactNumber',
  'primaryPhysician', 'insuranceProvider', 'insurancePolicyNumber', 'privacyConsent'
];

let allFieldsPresent = true;
for (const field of requiredFields) {
  const hasField = registerParams[field] !== undefined && registerParams[field] !== null;
  console.log(`  ✓ ${field}: ${hasField ? 'Present' : 'MISSING!'} (value: ${JSON.stringify(registerParams[field])})`);
  if (!hasField) allFieldsPresent = false;
}

console.log("\n5. APPWRITE DOCUMENT CREATION:");
if (allFieldsPresent) {
  console.log("✅ All required fields present - Document will be created successfully");
  console.log("\nDocument that will be sent to Appwrite:");
  console.log(JSON.stringify({
    database: "DATABASE_ID",
    collection: "PATIENT_COLLECTION_ID",
    document: registerParams
  }, null, 2));
} else {
  console.log("❌ Missing required fields - Appwrite will reject this request");
}

console.log("\n6. AFTER SUCCESSFUL REGISTRATION:");
console.log("  - User redirected to: /guests/user_abc123/new-appointment");
console.log("  - Guest document created with ID: [generated by Appwrite]");
console.log("  - Ready to make reservations");

console.log("\n=== REGISTRATION SIMULATION END ===");